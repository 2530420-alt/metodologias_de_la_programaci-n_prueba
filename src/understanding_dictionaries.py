# Simple dictionary
alien_0 = {'color': 'green', 'points': 5}

# The simpliest dictionary
alien_1 = {"color": "yellow"}

# Accessing values in a dictionary
print(alien_1['color'])
print(alien_0['points'])

# Empty dictionary
alien_2 = {}

# modifying values in a dictionary
alien_2 = {"color": "blue"}
alien_2["color"] = 'red'

# Adding new key-value pairs to a dictionary
alien_2["x_position"] = 0
alien_2["y_position"] = 25

print(alien_2)

# Dictionary to store similar objects
favorite_languages = {
    'charly': 'python',
    'anna': 'c',
    'edward': 'ruby',
    'phil': 'python',}

# print (f"Sarah favorite language is {favorite_languages['sarah'].title()}.")

# Looping through all key-value pairs in a dictionary
for name, language in favorite_languages.items():
    print(f"{name.title()}'s favorite language is {language.title()}.")

# looping through all the keys and values separately
for name in favorite_languages.keys():
    print(name.title())

for language in favorite_languages.values():
    print(language.title())

